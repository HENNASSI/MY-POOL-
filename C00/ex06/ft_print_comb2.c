#include <unistd.h>

void	print(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	int	n1;
	int	n2;

	n1 = 0;
	while (n1 <= 98)
	{
		n2 = n1 + 1;
		while (n2 <= 99)
		{
			print((n1 / 10) + '0');
			print((n1 % 10) + '0');
			print(' ');
			print((n2 / 10) + '0');
			print((n2 % 10) + '0');
			if (n1 != 98)
			{
				write(1, ", ", 2);
			}
		n2++;
		}
	n1++;
	}
}
